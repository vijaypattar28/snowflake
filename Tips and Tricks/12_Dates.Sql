// Date queries dealing with months

SELECT 
 DATEADD('MONTH',-1,DATE_TRUNC('MONTH',GETDATE()))                      AS PREVIOUS_MONTH_FIRST_DAY
,DATEADD('DAY', -1, DATE_TRUNC('MONTH',GETDATE()))                      AS PREVIOUS_MONTH_LAST_DAY  
,DATE_TRUNC('MONTH',GETDATE())                                          AS CURRENT_MONTH_FIRST_DAY  
,DATEADD('DAY', -1 , DATEADD(MONTH, +1,DATE_TRUNC('MONTH',GETDATE())))  AS CURRENT_MONTH_LAST_DAY   
,DATEADD('MONTH',1,DATE_TRUNC('MONTH',GETDATE()))                       AS NEXT_MONTH_FIRST_DAY     
,DATEADD('DAY',11, DATEADD('MONTH',+2,DATE_TRUNC('MONTH',GETDATE())))   AS NEXT_MONTH_LAST_DAY

// Date queries dealing with quarters

SELECT
 DATEADD('QUARTER', -1, DATE_TRUNC('QUARTER', GETDATE()))	                                   AS PREVIOUS_QUARTER_FIRST_DAY
,DATEADD('DAY', -1, DATE_TRUNC('QUARTER', GETDATE()))                                          AS PREVIOUS_QUARTER_LAST_DAY
,DATE_TRUNC('QUARTER', GETDATE())                                                              AS CURRENT_QUARTER_FIRST_DAY
,DATEADD('DAY', -1, DATEADD('QUARTER', 1, DATE_TRUNC('QUARTER', GETDATE())))                   AS CURRENT_QUARTER_LAST_DAY
,DATEADD('QUARTER',1,DATE_TRUNC('QUARTER', GETDATE()))                                         AS NEXT_QUARTER_FIRST_DAY
,DATEADD('DAY',-1,DATEADD('QUARTER',2,DATE_TRUNC('QUARTER', GETDATE())))                       AS NEXT_QUARTER_LAST_DAY
,DATE_TRUNC('YEAR',DATEADD(YEAR, -1, GETDATE()))                                               AS PREVIOUS_YEAR_Q1_FIRST_DAY
,DATEADD('DAY', -1, DATEADD('QUARTER',1,DATE_TRUNC('YEAR',DATEADD(YEAR, -1, GETDATE()))))      AS PREVIOUS_YEAR_Q1_LAST_DAY
,DATEADD('QUARTER',1,DATE_TRUNC('YEAR',DATEADD(YEAR, -1, GETDATE())))                          AS PREVIOUS_YEAR_Q2_FIRST_DAY
,DATEADD('DAY', -1, DATEADD('QUARTER',2,DATE_TRUNC('YEAR',DATEADD(YEAR, -1, GETDATE()))))      AS PREVIOUS_YEAR_Q2_LAST_DAY
,DATEADD('QUARTER',+2,DATE_TRUNC('YEAR',DATEADD(YEAR, -1, GETDATE())))                         AS PREVIOUS_YEAR_Q3_FIRST_DAY 
,DATEADD('DAY', -1, DATEADD('QUARTER',3,DATE_TRUNC('YEAR',DATEADD(YEAR, -1, GETDATE()))))      AS PREVIOUS_YEAR_Q3_LAST_DAY
,DATEADD('QUARTER',3,DATE_TRUNC('YEAR',DATEADD(YEAR, -1, GETDATE())))                          AS PREVIOUS_YEAR_Q4_FIRST_DAY 
,DATEADD('DAY', -1, DATEADD('QUARTER',4,DATE_TRUNC('YEAR',DATEADD(YEAR, -1, GETDATE()))))      AS PREVIOUS_YEAR_Q4_LAST_DAY
,DATE_TRUNC('YEAR',DATEADD(YEAR,  0, GETDATE()))                                               AS CURRENT_YEAR_Q1_FIRST_DAY 
,DATEADD('DAY', -1, DATEADD('QUARTER',1,DATE_TRUNC('YEAR',DATEADD(YEAR,  0, GETDATE()))))      AS CURRENT_YEAR_Q1_LAST_DAY 
,DATEADD('QUARTER',1,DATE_TRUNC('YEAR',DATEADD(YEAR,  0, GETDATE())))                          AS CURRENT_YEAR_Q2_FIRST_DAY 
,DATEADD('DAY', -1, DATEADD('QUARTER',2,DATE_TRUNC('YEAR',DATEADD(YEAR,  0, GETDATE()))))      AS CURRENT_YEAR_Q2_LAST_DAY
,DATEADD('QUARTER',2,DATE_TRUNC('YEAR',DATEADD(YEAR,  0, GETDATE())))                          AS CURRENT_YEAR_Q3_FIRST_DAY 
,DATEADD('DAY', 1, DATEADD('QUARTER',3,DATE_TRUNC('YEAR',DATEADD(YEAR,  0, GETDATE()))))       AS CURRENT_YEAR_Q3_LAST_DAY
,DATEADD('QUARTER',3,DATE_TRUNC('YEAR',DATEADD(YEAR,  0, GETDATE())))                          AS CURRENT_YEAR_Q4_FIRST_DAY 
,DATEADD('DAY', 1, DATEADD('QUARTER',4,DATE_TRUNC('YEAR',DATEADD(YEAR,  0, GETDATE()))))       AS CURRENT_YEAR_Q4_LAST_DAY

// Date queries dealing with years
SELECT
 DATE_TRUNC('YEAR',DATEADD(YEAR, -1, GETDATE()))   										    AS PREVIOUS_YEAR_FIRST_DAY
,DATEADD(DAY , -1, DATEADD(YEAR, 1, DATE_TRUNC('YEAR',DATEADD(YEAR, -1,	GETDATE()))))       AS PREVIOUS_YEAR_LAST_DAY
,DATE_TRUNC('YEAR',DATEADD(YEAR, 0,	GETDATE()))                                             AS CURRENT_YEAR_FIRST_DAY
,DATEADD(DAY , -1, DATEADD(YEAR, 1, DATE_TRUNC('YEAR',DATEADD(YEAR, 0, 	GETDATE()))))       AS CURRENT_YEAR_LAST_DAY
,DATE_TRUNC('YEAR',DATEADD(YEAR, 1,	GETDATE()))                                             AS NEXT_YEAR_FIRST_DAY   
,DATEADD(DAY , -1,DATE_TRUNC('YEAR',DATEADD(YEAR, 2,GETDATE())))                            AS NEXT_YEAR_LAST_DAY

// Date queries - other
SELECT
 GETDATE()                              AS NOW
,CAST(DATEADD(D,-1,GETDATE()) AS DATE)  AS YESTERDAY
,CAST(DATEADD(D,0,GETDATE()) AS DATE)   AS TODAY
,CAST(DATEADD(D,1,GETDATE()) AS DATE)   AS TOMORROW
